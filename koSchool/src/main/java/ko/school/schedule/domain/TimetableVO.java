package ko.school.schedule.domain;



public class TimetableVO {
	private int timenum;
	private String timetable;
	private String memberId;
	private int teachergrade;
	private int teacherclass;
	
	public int getTeachergrade() {
		return teachergrade;
	}
	public void setTeachergrade(int teachergrade) {
		this.teachergrade = teachergrade;
	}
	public int getTimenum() {
		return timenum;
	}
	public void setTimenum(int timenum) {
		this.timenum = timenum;
	}
	public String getTimetable() {
		return timetable;
	}
	public void setTimetable(String timetable) {
		this.timetable = timetable;
	}
	public String getMemberId() {
		return memberId;
	}
	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}
	public int getTeacherclass() {
		return teacherclass;
	}
	public void setTeacherclass(int teacherclass) {
		this.teacherclass = teacherclass;
	}
	
	
	
	
	
	
	
	
	
}
